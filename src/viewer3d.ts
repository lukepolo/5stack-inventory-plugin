// 3D weapon viewer — efficiency-first design:
// - three.js is DYNAMICALLY imported (own chunk, fetched only on first 3D use)
// - model availability is a cached HEAD check (no model file → silent 2D fallback)
// - parsed GLTFs live in a small LRU cache; only ONE live viewer exists at a
//   time (the Focus view), so a fresh lightweight renderer per mount is cheaper
//   than keeping a GL context alive globally. Snapshot-to-image for grid cards
//   (shared offscreen renderer + IndexedDB cache) is phase 2.
//
// Stickers render as surface-projected decals (three's DecalGeometry) and are
// DRAGGABLE in interactive mode — positions round-trip through the game's own
// sticker-offset space (cs2-lib per-weapon bounds). The charm is a verlet
// pendulum hanging off the body: it sways idly, reacts to placement changes,
// and can be grabbed/flung with the pointer.
//
// Skin rendering is a faithful port of CS2's compositing pipeline — see
// paintComposite.ts for the shader and its provenance.
