/**
 * Thrown when a mount is cancelled. Callers check `name === "AbortError"` and
 * swallow it — a cancelled mount is the expected outcome of closing a modal
 * quickly, not a failure worth surfacing to the user.
 */
export class ViewerAborted extends Error {
  override name = "AbortError";
  constructor() {
    super("viewer mount aborted");
  }
}
const throwIfAborted = (signal?: AbortSignal) => {
  if (signal?.aborted) throw new ViewerAborted();
};

/** Who is waiting on this build. Interactive = a human is looking at a spinner. */
export type BuildPriority = "interactive" | "background";

/**
 * One build at a time, process-wide, interactive first.
 *
 * Building a viewer creates a WebGLRenderer, and every live context counts
 * against the browser's cap (mobile Safari starts shedding the oldest once too
 * many exist — see onContextLost). Opening and closing the modal quickly used
 * to start a build per open, all racing to completion, each allocating its own
 * context and compositing a full paint job on the GPU before the stale ones
 * were discarded. N contexts and N composites for one visible viewer.
 *
 * Serialising the build phase bounds that at one in-flight context regardless
 * of how fast the user clicks. It also makes cancellation actually pay off: a
 * mount aborted while queued never starts, so the work is skipped outright
 * rather than done and thrown away.
 *
 * A single FIFO chain would then have put the user behind the card backfill —
 * open the modal while a bake is queued and you wait for the bake first, which
 * is the wrong way round: nobody is waiting on a bake, and somebody is always
 * waiting on a modal. So interactive builds jump the queue, AND preempt a bake
 * that already holds the lane. Aborting a bake is free: snapshotModelNow
 * catches and returns null, and generateRenderNow reads null as "retry later"
 * by dropping the id back out of renderedIds.
 */
type BuildTask = {
  priority: BuildPriority;
  signal?: AbortSignal;
  run: (signal: AbortSignal) => Promise<unknown>;
  resolve: (v: never) => void;
  reject: (e: unknown) => void;
};
let activeBuild: { priority: BuildPriority; preempt: AbortController } | null = null;
const pendingBuilds: BuildTask[] = [];
let buildsInFlight = 0;

function queueBuild<T>(
  priority: BuildPriority,
  signal: AbortSignal | undefined,
  run: (signal: AbortSignal) => Promise<T>,
): Promise<T> {
  return new Promise<T>((resolve, reject) => {
    pendingBuilds.push({ priority, signal, run, resolve: resolve as never, reject });
    // Don't make a human wait behind card art. The bake dies at its next
    // checkpoint and is retried later.
    if (priority === "interactive" && activeBuild?.priority === "background") {
      activeBuild.preempt.abort();
    }
    pumpBuilds();
  });
}

function pumpBuilds(): void {
  if (activeBuild || !pendingBuilds.length) return;
  // Interactive first; FIFO within a priority (findIndex keeps insertion order).
  const found = pendingBuilds.findIndex((t) => t.priority === "interactive");
  const [task] = pendingBuilds.splice(found >= 0 ? found : 0, 1);
  // Cancelled while queued — the case the queue exists to make cheap. Nothing
  // was allocated, so there is nothing to unwind.
  if (task.signal?.aborted) {
    task.reject(new ViewerAborted());
    pumpBuilds();
    return;
  }
  // The build sees ONE signal, fired by either the caller cancelling or us
  // preempting. Forwarding beats AbortSignal.any() here only because it needs
  // no feature detection; the `once` listener dies with the task.
  const preempt = new AbortController();
  task.signal?.addEventListener("abort", () => preempt.abort(), { once: true });
  activeBuild = { priority: task.priority, preempt };
  buildsInFlight++;
  task
    .run(preempt.signal)
    .then(task.resolve as (v: unknown) => void, task.reject)
    .finally(() => {
      buildsInFlight--;
      activeBuild = null;
      pumpBuilds();
    });
}

