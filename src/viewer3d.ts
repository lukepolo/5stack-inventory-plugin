// Crop a snapshot PNG to its visible content (+small margin). The raw frame
// is 640x480 with large transparent borders — next to tightly-cropped catalog
// art the gun looked shrunken inside identical <img> boxes.
//
// Aspect cap: cards fit these with object-contain, so a crop much wider than
// its box goes full-bleed while normal crops stop at the height cap — the gun
// in the slim crop draws ~25% longer than its neighbors. Long guns cluster at
// ~3.2-3.5; only near-horizontal poses (the Nova) land way above. Padding those
// back down to the cluster keeps every gun at the same on-screen scale.
const MAX_CROP_ASPECT = 3.6;
async function cropToContent(blob: Blob, margin = 0.03): Promise<Blob | null> {
  try {
    const img = await createImageBitmap(blob);
    const cv = document.createElement("canvas");
    cv.width = img.width;
    cv.height = img.height;
    const ctx = cv.getContext("2d", { willReadFrequently: true })!;
    ctx.drawImage(img, 0, 0);
    const d = ctx.getImageData(0, 0, cv.width, cv.height).data;
    let minX = cv.width;
    let minY = cv.height;
    let maxX = -1;
    let maxY = -1;
    for (let y = 0; y < cv.height; y++) {
      for (let x = 0; x < cv.width; x++) {
        if (d[(y * cv.width + x) * 4 + 3] > 8) {
          if (x < minX) minX = x;
          if (x > maxX) maxX = x;
          if (y < minY) minY = y;
          if (y > maxY) maxY = y;
        }
      }
    }
    if (maxX < 0) return blob; // fully transparent — keep as-is
    const mx = Math.round((maxX - minX + 1) * margin);
    const my = Math.round((maxY - minY + 1) * margin);
    minX = Math.max(0, minX - mx);
    minY = Math.max(0, minY - my);
    maxX = Math.min(cv.width - 1, maxX + mx);
    maxY = Math.min(cv.height - 1, maxY + my);
    const w = maxX - minX + 1;
    const h = maxY - minY + 1;
    const padY = w / h > MAX_CROP_ASPECT ? Math.ceil((w / MAX_CROP_ASPECT - h) / 2) : 0;
    const out = document.createElement("canvas");
    out.width = w;
    out.height = h + padY * 2;
    out.getContext("2d")!.drawImage(cv, minX, minY, w, h, 0, padY, w, h);
    return await new Promise<Blob | null>((resolve) => out.toBlob(resolve, "image/png"));
  } catch {
    return blob;
  }
}

// One-shot offscreen snapshot: mounts the REAL viewer (paint, wear, seed,
// sticker decals, charm) in a hidden container, lets attachments settle,
// grabs a frame, crops it to the content, and tears down. Cards get exactly
// what the 3D view shows, framed as tight as catalog art.
// Every snapshot burns a WebGL context for ~1s. Callers arrive from independent
// paths (the bake queue, the craft preview's debounced watch) that don't know
// about each other, so a wear/seed slider drag used to stack overlapping
// contexts until mobile Safari hit its cap and started killing them. One global
// lane means the offscreen cost is exactly one context no matter who asks.
let snapshotLane: Promise<unknown> = Promise.resolve();

// Onscreen viewers currently mounted. A live viewer owns a WebGL context, a
// per-frame rAF loop and the charm's verlet physics, all trying to hold 60fps.
// A bake mounts a SECOND context, loads a model and composites paint for ~1s —
// so backfilling a page of card renders while you are orbiting or dragging a
// charm makes the thing you are actually looking at crawl. Background bakes
// wait for the last viewer to go away: deferred, never dropped. Foreground
// snapshots (the craft modal's live 2D preview) are exempt — that preview IS
// what the user is looking at, and gating it would mean it never renders while
// its own modal is open.
let liveViewers = 0;
let idleWaiters: (() => void)[] = [];
/** Resolves once no 3D viewer is onscreen. Lets a queue park work that would
 *  otherwise compete with a viewer for the GPU, and show it as queued rather
 *  than pretending it is already underway. */
export const viewersIdle = () =>
  liveViewers === 0
    ? Promise.resolve()
    : new Promise<void>((resolve) => {
        idleWaiters.push(resolve);
      });
function releaseViewer() {
  if (--liveViewers > 0) return;
  const waiters = idleWaiters;
  idleWaiters = [];
  for (const w of waiters) w();
}
/**
 * TEMPORARY (mdebug): how many GL contexts are actually live, and how many
 * builds are in flight. The number that matters for "will this kill the
 * browser" — a leak shows as a count that climbs and never comes back down.
 */
export const viewerStats = () => ({
  live: liveViewers,
  building: buildsInFlight,
  queued: pendingBuilds.length,
  lane: activeBuild?.priority ?? null,
});

export function snapshotModel(
  model: string,
  opts: Omit<ViewerOpts, "interactive" | "still">,
  /** Checked once the lane frees up — return false if this result is already
   *  stale (e.g. the user kept dragging the wear slider) to skip the work. */
  stillWanted?: () => boolean,
  /** Opportunistic cache fill rather than something the user is waiting on —
   *  hold it until no viewer is onscreen. */
  background = false,
): Promise<Blob | null> {
  // Only the backfill rides the background lane. The craft modal's live 2D
  // preview is also a snapshot, but the user IS waiting on it, so it stays
  // interactive and is never preempted.
  const priority: BuildPriority = background ? "background" : "interactive";
  // Each attempt re-does the idle wait AND re-takes the lane from scratch. The
  // retry deliberately sits out here rather than inside snapshotModelNow: a
  // preempted bake waiting on viewersIdle while still holding snapshotLane
  // would block the craft preview behind it for as long as the modal's own
  // viewer stayed open — the very thing the wait-before-the-lane ordering
  // below exists to prevent.
  const attempt = (n: number): Promise<Blob | null> =>
    (background ? viewersIdle() : Promise.resolve())
      .then(() => {
        // The idle wait happens BEFORE taking the lane, never inside it.
        const run = snapshotLane.then(() =>
          stillWanted && !stillWanted() ? null : snapshotModelNow(model, opts, priority),
        );
        snapshotLane = run.catch(() => null);
        return run;
      })
      .then((res) => (res === PREEMPTED ? (n < BAKE_PREEMPT_RETRIES ? attempt(n + 1) : null) : res));
  return attempt(0);
}

// Offscreen bakes actually RUNNING right now (as opposed to parked behind
// viewersIdle). A bake mounts its own WebGL context, so while one is in flight
// it competes with every live viewer for the GPU — which shows up in a viewer
// as a spiking `draw` with an idle CPU frame, and is otherwise invisible.
let snapshotsInFlight = 0;
export const bakesInFlight = () => snapshotsInFlight;

/**
 * How many times a preempted bake will re-queue itself before giving up.
 *
 * A bake has no caller signal, so ANY abort it sees is a preemption — and a
 * preemption must not read as failure. Returning null would let generateRender
 * drop the id and leave that card unbaked until it remounted, so a user who
 * opened a few modals during the backfill would be left with permanent holes in
 * the grid. Retrying keeps preemption invisible to the caller: a bake yields the
 * GPU, waits for the viewers to go, and finishes later.
 *
 * Bounded so a user who keeps a viewer open forever can't spin this.
 */
const BAKE_PREEMPT_RETRIES = 3;
/** Distinct from null: "yielded the GPU, ask again" rather than "this failed". */
const PREEMPTED = Symbol("preempted");

async function snapshotModelNow(
  model: string,
  opts: Omit<ViewerOpts, "interactive" | "still">,
  priority: BuildPriority = "interactive",
): Promise<Blob | null | typeof PREEMPTED> {
  snapshotsInFlight++;
  const holder = document.createElement("div");
  holder.style.cssText = "position:fixed;left:-10000px;top:0;width:640px;height:480px;pointer-events:none;";
  document.body.appendChild(holder);
  let handle: ViewerHandle | null = null;
  try {
    handle = await mountViewer(holder, model, { ...opts, interactive: false, still: true, frame: "fit", priority });
    // Decal texture loads + a few physics frames for the charm to hang.
    await new Promise((r) => setTimeout(r, 750));
    const raw = await handle.snapshot();
    return raw ? await cropToContent(raw) : raw;
  } catch (e) {
    // A bake passes no signal of its own, so any abort it sees is a preemption
    // by an interactive mount. Reported as PREEMPTED so the caller retries —
    // null would let generateRender drop the id and leave that card unbaked
    // until it remounted, so a user who opened a few modals mid-backfill would
    // be left with permanent holes in the grid.
    if (priority === "background" && (e as Error)?.name === "AbortError") return PREEMPTED;
    return null;
  } finally {
    snapshotsInFlight--;
    handle?.dispose();
    holder.remove();
  }
}

