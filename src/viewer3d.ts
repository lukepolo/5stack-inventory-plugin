// Encoded per PATH SEGMENT, not whole: shared models live in subdirectories
// (extra/stattrak_module), and encodeURIComponent on the whole key turns the
// separator into %2F and 404s.
export const modelUrlFor = (model: string) =>
  `${API_ORIGIN}/models/${model.split("/").map(encodeURIComponent).join("/")}.glb`;

// HEAD-check whether a .glb exists for this weapon model; results are cached
// for the session so grids/toggles can query freely.
const availability = new Map<string, Promise<boolean>>();
// The same answers, already settled. `hasModel` can only ever be awaited, and
// awaiting yields to the renderer even on a cache hit — which is why the craft
// modal painted its 2D still first and then swapped to 3D a frame later, on
// every open, for a model it already knew about. Callers that must decide
// before first paint peek here and fall back to the promise on a real miss.
const settled = new Map<string, boolean>();
/** Resolved availability, or null if not yet known. Never triggers a fetch. */
export function hasModelSync(model: string): boolean | null {
  return settled.has(model) ? settled.get(model)! : null;
}
export function hasModel(model: string): Promise<boolean> {
  let cached = availability.get(model);
  if (!cached) {
    cached = fetch(modelUrlFor(model), { method: "HEAD" })
      .then((res) => res.ok && !(res.headers.get("content-type") ?? "").includes("text/html"))
      .catch(() => false)
      .then((ok) => {
        settled.set(model, ok);
        return ok;
      });
    availability.set(model, cached);
  }
  return cached;
}

// Parsed-GLTF LRU (the heavy part — geometry + textures).
const gltfCache = new Map<string, Promise<import("three/examples/jsm/loaders/GLTFLoader.js").GLTF>>();
const GLTF_CACHE_MAX = 8;
async function loadGltf(model: string) {
  const existing = gltfCache.get(model);
  if (existing) {
    // refresh LRU order
    gltfCache.delete(model);
    gltfCache.set(model, existing);
    return existing;
  }
  const { GLTFLoader } = await loadThree();
  const loader = new GLTFLoader();
  // Textures are flat-copied to the models ROOT, but a .glb in a subdirectory
  // (extra/, knives/) references them by RELATIVE uri — which would resolve to
  // its own folder and 404, rendering the model untextured white. Point the
  // resource path back at the root so those resolve. Harmless for top-level
  // models, whose folder already IS the root.
  if (model.includes("/")) loader.setResourcePath(`${API_ORIGIN}/models/`);
  const entry = loader.loadAsync(modelUrlFor(model));
  gltfCache.set(model, entry);
  if (gltfCache.size > GLTF_CACHE_MAX) {
    const oldest = gltfCache.keys().next().value;
    if (oldest) gltfCache.delete(oldest);
  }
  return entry;
}

// Shared texture cache (sticker/charm images are tiny and reused across
// viewer mounts — never disposed with a viewer).
const texCache = new Map<string, Promise<import("three").Texture>>();
async function loadTexture(url: string) {
  let cached = texCache.get(url);
  if (!cached) {
    cached = loadThree().then(async ({ THREE }) => {
      const tex = await new THREE.TextureLoader().loadAsync(url);
      tex.colorSpace = THREE.SRGBColorSpace;
      return tex;
    });
    texCache.set(url, cached);
  }
  return cached;
}

// Charm art ships with generous transparent padding — hanging the raw quad by
// its top edge leaves the charm floating in air below its anchor. Crop each
// image to its alpha bounding box once (cached) so the art's own connector
// sits flush against the attachment point.
const charmTexCache = new Map<string, Promise<{ tex: import("three").Texture; aspect: number } | null>>();
function loadCharmTexture(url: string) {
  let cached = charmTexCache.get(url);
  if (!cached) {
    cached = (async () => {
      try {
        const { THREE } = await loadThree();
        const img = await loadBitmap(url);
        const cv = document.createElement("canvas");
        cv.width = img.width;
        cv.height = img.height;
        const ctx = cv.getContext("2d", { willReadFrequently: true })!;
        ctx.drawImage(img, 0, 0);
        const d = ctx.getImageData(0, 0, cv.width, cv.height).data;
        let minX = cv.width;
        let minY = cv.height;
        let maxX = -1;
        let maxY = -1;
        for (let y = 0; y < cv.height; y++) {
          for (let x = 0; x < cv.width; x++) {
            // > 48: faint halo/shadow pixels above the art used to stretch the
            // bbox, leaving a visual gap between the cord and the charm.
            if (d[(y * cv.width + x) * 4 + 3] > 48) {
              if (x < minX) minX = x;
              if (x > maxX) maxX = x;
              if (y < minY) minY = y;
              if (y > maxY) maxY = y;
            }
          }
        }
        if (maxX < 0) {
          minX = minY = 0;
          maxX = cv.width - 1;
          maxY = cv.height - 1;
        }
        const w = maxX - minX + 1;
        const h = maxY - minY + 1;
        const crop = document.createElement("canvas");
        crop.width = w;
        crop.height = h;
        crop.getContext("2d")!.drawImage(cv, minX, minY, w, h, 0, 0, w, h);
        const tex = new THREE.CanvasTexture(crop);
        tex.colorSpace = THREE.SRGBColorSpace;
        return { tex, aspect: w / h };
      } catch {
        return null;
      }
    })();
    charmTexCache.set(url, cached);
  }
  return cached;
}

